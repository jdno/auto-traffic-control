//! Auto Traffic Control
//!
//! Auto Traffic Control is a video game played by programming. The player's task is to create a
//! program that can safely manage the airspace above two airports.
//!
//! The game is designed to provide an open-ended sandbox that players can use to freely practice
//! programming. It provides a language-agnostic gRPC API, giving players free choice of
//! programming language or paradigm.
